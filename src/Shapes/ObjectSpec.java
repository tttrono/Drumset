package Shapes;

import java.awt.Color;

public class ObjectSpec {

	public double x;
	public double y;
	public double width;
	public double height;
	
	public float stroke;
	public Color color;
	
	public ObjectSpec(double x, double y, double w, double h, float s, Color c) {
		
		this.x = x;
		this.y = y;
		width = w;
		height = h;
		
		stroke = s;
		color = c;
	}
	
	

}
